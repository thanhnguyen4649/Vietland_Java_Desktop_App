    public void fillDataTABLE() {
        DefaultTableModel model = (DefaultTableModel) tbGia.getModel();
        model.setRowCount(0);
        for (Price p : dDAO.getAllPrice()) {
            Object rowData[] = new Object[7];
            rowData[0] = p.getBDS().getMaBDS();
            rowData[1] = p.getBDS().getSDT();
            rowData[2] = p.getGiaChuNha();
            rowData[3] = p.getGiaKhach();
            rowData[4] = p.getGiaGDCu();
            String tb = String.format("%d", Math.round(p.getGiaMucTieu()));
            rowData[5] = tb;
            rowData[6] = p.getTiemNang();
            model.addRow(rowData);
        }
    }
    
