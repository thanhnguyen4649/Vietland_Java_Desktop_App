    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        if (txtFindMaBDS.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã BDS");
        } else {
            BDS bds = bdsDAO.getBDSByID(txtFindMaBDS.getText());
            if (bds != null) {
                Price d = dDAO.getOnePriceByMaBDS(bds.getMaBDS());
                if (d != null) {
                    setModel(d);
                } else {
                    txtMaBDS.setText(bds.getMaBDS());
                    txtSDT.setText(bds.getSDT());
                    txtGiaChuNha.setText("-1");
                    txtGiaKhach.setText("-1");
                    txtGiaGDCu.setText("-1");
                    lblGiaMucTieu.setText("0.0");
                }
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

