    public Price getPriceAtPosition(int pos) {
        Price p = new Price();
        BDS bds = new BDS();
        bds.setMaBDS(tbGia.getValueAt(pos, 0).toString());
        bds.setSDT(tbGia.getValueAt(pos, 1).toString());
        p.setBDS(bds);
        p.setGiaChuNha(Long.parseLong(tbGia.getValueAt(pos, 2).toString()));
        p.setGiaKhach(Long.parseLong(tbGia.getValueAt(pos, 3).toString()));
        p.setGiaGDCu(Long.parseLong(tbGia.getValueAt(pos, 4).toString()));
        return p;
    }

    

