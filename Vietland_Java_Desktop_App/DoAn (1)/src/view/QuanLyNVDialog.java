    public NV getNVAtPosition(int pos) {
        NV nv = new NV();
        nv.setMaNV(tbNV.getValueAt(pos, 0).toString());
        nv.setTenNV(tbNV.getValueAt(pos, 1).toString());
        nv.setLuongCB(Long.parseLong(tbNV.getValueAt(pos, 2).toString()));
        nv.setThuong(Long.parseLong(tbNV.getValueAt(pos, 3).toString()));

        try {
            Date ngayDangKy = dateFormat.parse(tbNV.getValueAt(pos, 4).toString());
            nv.setNgayDangKy(ngayDangKy);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Định dạng ngày tháng không hợp lệ!");
        }

        boolean phongban = false; // Mặc định là Cần Bán
        if (tbNV.getValueAt(pos, 5).toString().equalsIgnoreCase("MangThue")) {
            phongban = true; // Cho thuê
        }
        nv.setPhongBan(phongban);
        
        nv.setBaoCaoNhanVien(tbNV.getValueAt(pos, 6).toString());
        nv.setMaKH(tbNV.getValueAt(pos, 7).toString());

        return nv;
    }


