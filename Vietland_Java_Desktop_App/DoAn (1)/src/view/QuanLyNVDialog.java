    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if (validateForm()) {
            NV nv = null;
            try {
                nv = getModel();
            } catch (ParseException ex) {
                Logger.getLogger(QuanLyNVDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (dao.getOneNVByMaNV(nv.getMaNV()) != null) {
                JOptionPane.showMessageDialog(this, "Mã bđs này đã tồn tại");
            } else {
                if (dao.add(nv) > 0) {
                    fillDataTABLE();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Xin vui lòng kiểm tra thông tin");
        }

    }//GEN-LAST:event_btnLuuActionPerformed

