    public void fillDataTABLE() {
    DefaultTableModel model = (DefaultTableModel) tbNV.getModel();
    model.setRowCount(0); // Xóa tất cả các dòng trong bảng

    for (NV nv : dao.getAllNV()) {
        Object[] rowData = new Object[8];
        rowData[0] = nv.getMaNV();
        rowData[1] = nv.getTenNV();
        rowData[2] = nv.getLuongCB();
        rowData[3] = nv.getThuong();

        // Kiểm tra null trước khi định dạng ngày
        if (nv.getNgayDangKy() != null) {
            rowData[4] = dateFormat.format(nv.getNgayDangKy());
        }

        rowData[5] = nv.isPhongBan() ? "MangThue" : "MangBan"; 
        rowData[6] = nv.getBaoCaoNhanVien();
        //rowData[7] = Math.round(nv.getTongLuong());

        rowData[7] = nv.getMaKH();
        

        model.addRow(rowData);
    }
}


